scraper = cffi_requests

class Handler(http.server.SimpleHTTPRequestHandler):
    def do_GET(self):
        if self.path.startswith("/api/bikes"):
            self.get_bikes()
        elif self.path == "/" or self.path.startswith("/index.html") or self.path.startswith("/?"):
            self.path = "/index.html"
            super().do_GET()
        else:
            super().do_GET()

    def get_bikes(self):
        zone = "wm" if "zone=wm" in self.path else "rbkc6" if "zone=rbkc6" in self.path else "lam6" if "zone=lam6" in self.path else "bre1"
        zones = BRE1_ZONES if zone == "bre1" else RBKC6_ZONES if zone == "rbkc6" else LAM6_ZONES if zone == "lam6" else WM_ZONES
        filter_fn = in_bre1 if zone == "bre1" else in_rbkc6 if zone == "rbkc6" else in_lam6 if zone == "lam6" else in_wm
        hdrs = {"Authorization":"Bearer "+TOKEN,"Platform":"iOS","App-Version":"3.248.1","Content-Type":"application/json","Accept":"application/json"}
        all_bikes = []
        seen_id = set()
        seen_pos = set()
        for i, z in enumerate(zones):
            try:
                r = scraper.get("https://web-production.lime.bike/api/rider/v2/map/bike_pins", params=dict(z, zoom=16.0), headers=hdrs, impersonate="chrome")
                pins = r.json().get("data",{}).get("attributes",{}).get("bike_pins",[])
                added = 0
                for pin in pins:
                    bid = pin.get("id","")
                    if bid in seen_id:
                        continue
                    seen_id.add(bid)
                    loc = pin.get("location",{})
                    lat = loc.get("latitude",0)
                    lng = loc.get("longitude",0)
                    pos = str(round(lat, 5)) + "," + str(round(lng, 5))
                    if pos in seen_pos:
                        continue
                    seen_pos.add(pos)
                    if filter_fn(lat, lng):
                        all_bikes.append({"id":bid,"lat":lat,"lng":lng,"type":pin.get("sub_type_name","unknown")})
                        added += 1
                print("Strip " + str(i+1) + " (" + zone + "): " + str(len(pins)) + " raw, " + str(added) + " new")
            except Exception as e:
                print("Strip " + str(i+1) + " error: " + str(e))
        out = json.dumps({"bikes":all_bikes,"count":len(all_bikes)})
        self.send_response(200)
        self.send_header("Content-Type","application/json")
        self.send_header("Access-Control-Allow-Origin","*")
        self.end_headers()
        self.wfile.write(out.encode())
        print("TOTAL (" + zone + "): " + str(len(all_bikes)) + " unique (from " + str(len(seen_id)) + " IDs)")

sys.stdout.flush()
